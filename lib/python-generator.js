// MEX to Python/TensorFlow Generator
// Generates complete, runnable Python code from MEX AST

function generatePython(ast, filename = 'mex_model') {
  const lines = [];
  let hasData = false;
  let hasModel = false;
  let hasLayers = [];
  let epochs = 100;
  let predictions = [];
  
  // First pass: collect all components
  for (const node of ast.body) {
    switch (node.type) {
      case 'DataPoints':
        hasData = true;
        break;
      case 'ModelDecl':
        hasModel = true;
        break;
      case 'LayerDecl':
        hasLayers.push(node);
        break;
      case 'TrainSection':
        epochs = node.epochs || 100;
        break;
      case 'PredictSection':
        predictions.push(node);
        break;
    }
  }
  
  // Header
  lines.push('# Generated by MEX → Python/TensorFlow');
  lines.push('# MEX is a learning language for ML');
  lines.push('# This converts your MEX code to production Python');
  lines.push('');
  
  // Imports
  lines.push('import tensorflow as tf');
  lines.push('import numpy as np');
  lines.push('');
  
  // Data section
  for (const node of ast.body) {
    if (node.type === 'DataPoints') {
      lines.push(generatePythonData(node));
      lines.push('');
    }
  }
  
  // Model section
  for (const node of ast.body) {
    if (node.type === 'ModelDecl') {
      lines.push(generatePythonModel(node, hasLayers));
      lines.push('');
    }
  }
  
  // Layer sections
  for (const node of ast.body) {
    if (node.type === 'LayerDecl') {
      lines.push(generatePythonLayer(node));
    }
  }
  if (hasLayers.length > 0) {
    lines.push('');
  }
  
  // Training section
  for (const node of ast.body) {
    if (node.type === 'TrainSection') {
      lines.push(generatePythonTrain(node));
      lines.push('');
    }
  }
  
  // Main execution block
  lines.push('');
  lines.push('# Run the model');
  lines.push('if __name__ == "__main__":');
  lines.push('    # Compile the model');
  lines.push('    model.compile(optimizer=\'sgd\', loss=\'mse\')');
  lines.push('');
  lines.push('    # Train the model');
  lines.push('    model.fit(xs, ys, epochs=EPOCHS, verbose=0)');
  lines.push('');
  
  if (predictions.length > 0) {
    lines.push('    # Make predictions');
    for (const pred of predictions) {
      const input = Array.isArray(pred.input) ? pred.input : [pred.input];
      lines.push('    pred = model.predict(np.array([[' + input.join(', ') + ']]))');
      lines.push('    print(f"Prediction for ' + input.join(', ') + ': {pred.numpy()[0][0]:.4f}")');
    }
  } else {
    lines.push('    # Make a sample prediction');
    lines.push('    # prediction = model.predict(np.array([[5]]))');
    lines.push('    # print(f"Prediction: {prediction.numpy()[0][0]:.4f}")');
  }
  
  return lines.join('\n');
}

function generatePythonData(node) {
  if (node.points && node.points.length > 0) {
    const inputs = node.points.map(p => p[0]);
    const outputs = node.points.map(p => p[1]);
    const lines = [];
    lines.push('# Data: ' + node.points.length + ' points');
    lines.push('xs = np.array([' + inputs.join(', ') + ']).reshape(-1, 1)');
    lines.push('ys = np.array([' + outputs.join(', ') + ']).reshape(-1, 1)');
    return lines.join('\n');
  }
  
  return '# Data section';
}

function generatePythonModel(node, layers) {
  const lines = [];
  const modelType = node.modelType || 'simple';
  
  if (modelType === 'simple' || modelType === 'linear') {
    lines.push('# Simple linear model');
    lines.push('model = tf.keras.Sequential([');
    lines.push('    tf.keras.layers.Dense(1, input_shape=[1])');
    lines.push('])');
    return lines.join('\n');
  }
  
  if (modelType === 'sequential') {
    lines.push('# Sequential model');
    lines.push('model = tf.keras.Sequential()');
    return lines.join('\n');
  }
  
  return '# ' + modelType + ' model';
}

function generatePythonLayer(node) {
  const units = node.units || 1;
  const activation = node.activation || 'linear';
  
  let actStr = activation;
  if (activation === 'relu') actStr = 'relu';
  else if (activation === 'sigmoid') actStr = 'sigmoid';
  else if (activation === 'tanh') actStr = 'tanh';
  else if (activation === 'softmax') actStr = 'softmax';
  
  const lines = [];
  lines.push('# Add dense layer: ' + units + ' units, ' + activation + ' activation');
  lines.push('model.add(tf.keras.layers.Dense(' + units + ', activation=\'' + actStr + '\'))');
  return lines.join('\n');
}

function generatePythonTrain(node) {
  const epochs = node.epochs || 100;
  const lines = [];
  lines.push('# Training configuration');
  lines.push('EPOCHS = ' + epochs);
  return lines.join('\n');
}

function generatePythonExpr(node) {
  if (node.type === 'Number') return String(node.value);
  if (node.type === 'String') return '"' + node.value + '"';
  if (node.type === 'Boolean') return node.value ? 'True' : 'False';
  if (node.type === 'Identifier') return node.name;
  if (node.type === 'BinaryOp') {
    const left = generatePythonExpr(node.left);
    const right = generatePythonExpr(node.right);
    const op = node.op === 'PLUS' ? '+' : node.op;
    return '(' + left + ' ' + op + ' ' + right + ')';
  }
  if (node.type === 'Call') {
    const args = node.args.map(arg => generatePythonExpr(arg)).join(', ');
    return node.name + '(' + args + ')';
  }
  return 'None';
}

module.exports = { generatePython };
